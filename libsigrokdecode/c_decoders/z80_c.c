static int reduce_bus(const uint8_t *pins, int start, int end)
{
    /* Match Python's reduce(lambda a, b: (a<<1)|b, reversed(bus)):
     * process from high index (MSB) to low index (LSB) */
    int val = 0;
    for (int i = end; i >= start; i--) {
        if (pins[i] == 0xFF) return -1;  /* unassigned */
        val = (val << 1) | pins[i];
    }
    return val;
}

static int signed_byte(int byte)
{
    return (byte < 128) ? byte : byte - 256;
}

static void format_hex(char *buf, size_t sz, int value, int width)
{
    char tmp[16];
    snprintf(tmp, sizeof(tmp), "%0*X", width, value);
    if (tmp[0] >= '0' && tmp[0] <= '9')
        snprintf(buf, sz, "%sH", tmp);
    else
        snprintf(buf, sz, "0%sH", tmp);
}

static void put_text(struct srd_decoder_inst *di, z80_priv *s,
                     uint64_t ss, int ann_idx, const char *text)
{
    c_put(di, ss, s->cur_samplenum, s->out_ann, ann_idx, text);
}

static void put_disasm(struct srd_decoder_inst *di, z80_priv *s, uint64_t samplenum)
{
    (void)samplenum;
    /* Format the mnemonic with operands */
    char text[256];
    const char *fmt = s->mnemonic;
    int pos = 0;

    for (int i = 0; fmt[i] && pos < (int)sizeof(text) - 32; i++) {
        if (fmt[i] == '{') {
            /* Parse format specifier */
            char field[8] = {0};
            char spec[8] = {0};
            int fi = 0, si = 0;
            i++; /* skip { */
            while (fmt[i] && fmt[i] != ':' && fmt[i] != '}' && fi < 7)
                field[fi++] = fmt[i++];
            if (fmt[i] == ':') {
                i++;
                while (fmt[i] && fmt[i] != '}' && si < 7)
                    spec[si++] = fmt[i++];
            }
            /* skip closing } */
            if (fmt[i] == '}') i++;

            if (strcmp(field, "r") == 0) {
                pos += snprintf(text + pos, sizeof(text) - pos, "%s", s->arg_reg);
            } else if (strcmp(field, "d") == 0) {
                pos += snprintf(text + pos, sizeof(text) - pos, "%+d", s->arg_dis);
            } else if (strcmp(field, "j") == 0) {
                pos += snprintf(text + pos, sizeof(text) - pos, "%+d", s->arg_dis + s->instr_len);
            } else if (strcmp(field, "i") == 0) {
                if (strcmp(spec, "02H") == 0) {
                    char hex[32];
                    format_hex(hex, sizeof(hex), s->arg_imm & 0xFF, 2);
                    pos += snprintf(text + pos, sizeof(text) - pos, "%s", hex);
                } else if (strcmp(spec, "04H") == 0) {
                    char hex[32];
                    format_hex(hex, sizeof(hex), s->arg_imm & 0xFFFF, 4);
                    pos += snprintf(text + pos, sizeof(text) - pos, "%s", hex);
                } else {
                    pos += snprintf(text + pos, sizeof(text) - pos, "%d", s->arg_imm);
                }
            } else if (strcmp(field, "ro") == 0) {
                if (strcmp(spec, "02X") == 0)
                    pos += snprintf(text + pos, sizeof(text) - pos, "%02X", s->arg_read & 0xFF);
                else if (strcmp(spec, "04X") == 0)
                    pos += snprintf(text + pos, sizeof(text) - pos, "%04X", s->arg_read & 0xFFFF);
                else
                    pos += snprintf(text + pos, sizeof(text) - pos, "%d", s->arg_read);
            } else if (strcmp(field, "wo") == 0) {
                if (strcmp(spec, "02X") == 0)
                    pos += snprintf(text + pos, sizeof(text) - pos, "%02X", s->arg_write & 0xFF);
                else if (strcmp(spec, "04X") == 0)
                    pos += snprintf(text + pos, sizeof(text) - pos, "%04X", s->arg_write & 0xFFFF);
                else
                    pos += snprintf(text + pos, sizeof(text) - pos, "%d", s->arg_write);
            }
            i--; /* for loop will increment */
        } else {
            text[pos++] = fmt[i];
        }
    }
    text[pos] = '\0';

    put_text(di, s, s->dasm_start, s->ann_dasm, text);
    s->ann_dasm = -1;
    s->dasm_start = samplenum;
}

static int z80_detect_cycle(const uint8_t *pins, int has_mreq, int has_iorq)
{
    int mreq = has_mreq ? pins[11] : 0;  /* default asserted (active low) when unassigned */
    int iorq = has_iorq ? pins[12] : 1;  /* default NOT asserted when unassigned */
    int m1 = pins[8];
    int rd = pins[9];
    int wr = pins[10];

    if (mreq == 0) {
        if (rd == 0)
            return (m1 == 0) ? CYCLE_FETCH : CYCLE_MEMRD;
        else if (wr == 0)
            return CYCLE_MEMWR;
    } else if (iorq == 0) {
        if (m1 == 0)
            return CYCLE_INTACK;
        else if (rd == 0)
            return CYCLE_IORD;
        else if (wr == 0)
            return CYCLE_IOWR;
    }
    return CYCLE_NONE;
}

static void z80_reset(struct srd_decoder_inst *di)
{
    if (!c_decoder_get_private(di)) {
        c_decoder_set_private(di, g_malloc0(sizeof(z80_priv)));
    }
    z80_priv *s = (z80_priv *)c_decoder_get_private(di);
    memset(s, 0, sizeof(z80_priv));
    s->out_ann = -1;
    s->prev_cycle = CYCLE_NONE;
    s->op_state = STATE_IDLE;
    s->bus_data = -1;
    s->pend_addr = -1;
    s->ann_data = -1;
    s->ann_dasm = -1;
}

static void z80_start(struct srd_decoder_inst *di)
{
    z80_priv *s = (z80_priv *)c_decoder_get_private(di);
    s->out_ann = c_reg_out(di, SRD_OUTPUT_ANN, "z80");
    s->has_mreq = c_has_ch(di, 11);
    s->has_iorq = c_has_ch(di, 12);
}

static void z80_decode(struct srd_decoder_inst *di)
{
    z80_priv *s = (z80_priv *)c_decoder_get_private(di);

    /* Process initial sample to detect starting cycle state */
    {
        int ret = c_wait(di, CW_END);
        if (ret != SRD_OK)
            return;
        s->cur_samplenum = di_samplenum(di);

        uint8_t pins[29];
        for (int i = 0; i < 29; i++)
            pins[i] = c_pin(di, i);

        int cycle = z80_detect_cycle(pins, s->has_mreq, s->has_iorq);
        if (cycle != CYCLE_NONE) {
            s->bus_data = reduce_bus(pins, 0, 7);
            s->addr_start = di_samplenum(di);
            s->pend_addr = reduce_bus(pins, 13, 28);
            s->data_start = di_samplenum(di);
        }
        s->prev_cycle = cycle;
    }

    while (1) {
        /* Wait for any control signal change — single combined wait */
        int ret;
        if (s->has_mreq && s->has_iorq)
            ret = c_wait(di, CW_E(8), CW_OR, CW_E(9), CW_OR, CW_E(10), CW_OR, CW_E(11), CW_OR, CW_E(12), CW_END);
        else if (s->has_mreq)
            ret = c_wait(di, CW_E(8), CW_OR, CW_E(9), CW_OR, CW_E(10), CW_OR, CW_E(11), CW_END);
        else if (s->has_iorq)
            ret = c_wait(di, CW_E(8), CW_OR, CW_E(9), CW_OR, CW_E(10), CW_OR, CW_E(12), CW_END);
        else
            ret = c_wait(di, CW_E(8), CW_OR, CW_E(9), CW_OR, CW_E(10), CW_END);
        if (ret != SRD_OK)
            return;
        s->cur_samplenum = di_samplenum(di);

        /* Read all pins */
        uint8_t pins[29];
        for (int i = 0; i < 29; i++)
            pins[i] = c_pin(di, i);

        int cycle = z80_detect_cycle(pins, s->has_mreq, s->has_iorq);

        if (cycle != CYCLE_NONE)
            s->bus_data = reduce_bus(pins, 0, 7);

        if (cycle != s->prev_cycle) {
            if (s->prev_cycle == CYCLE_NONE) {
                /* Cycle begin */
                if (s->pend_addr != -1) {
                    char addr_str[8];
                    snprintf(addr_str, sizeof(addr_str), "%04X", s->pend_addr);
                    put_text(di, s, s->addr_start, ANN_ADDR, addr_str);
                }
                s->addr_start = di_samplenum(di);
                s->pend_addr = reduce_bus(pins, 13, 28);
            } else if (cycle == CYCLE_NONE) {
                /* Cycle end */
                s->instr_len++;

                /* Execute state machine */
                switch (s->op_state) {
                case STATE_IDLE:
                    if (s->prev_cycle != CYCLE_FETCH) {
                        s->op_state = STATE_IDLE;
                        break;
                    }
                    s->want_dis = 0; s->want_imm = 0;
                    s->want_read = 0; s->want_write = 0;
                    s->want_wr_be = 0; s->op_repeat = 0;
                    s->arg_dis = 0; s->arg_imm = 0;
                    s->arg_read = 0; s->arg_write = 0;
                    s->arg_reg[0] = '\0';
                    s->mnemonic[0] = '\0';
                    s->instr_pend = 0; s->read_pend = 0; s->write_pend = 0;
                    s->dasm_start = di_samplenum(di);
                    s->op_prefix = 0;
                    s->instr_len = 0;
                    if (s->bus_data == 0xCB || s->bus_data == 0xED ||
                        s->bus_data == 0xDD || s->bus_data == 0xFD)
                        s->op_state = STATE_PRE1;
                    else
                        s->op_state = STATE_OPCODE;
                    break;

                case STATE_PRE1:
                    if (s->prev_cycle != CYCLE_FETCH) {
                        snprintf(s->mnemonic, sizeof(s->mnemonic), "Prefix not followed by fetch");
                        s->ann_dasm = ANN_WARN;
                        s->op_state = STATE_RESTART;
                        break;
                    }
                    s->op_prefix = s->pend_data;
                    if (s->op_prefix == 0xDD || s->op_prefix == 0xFD) {
                        if (s->bus_data == 0xCB)
                            s->op_state = STATE_PRE2;
                        else if (s->bus_data == 0xDD || s->bus_data == 0xED || s->bus_data == 0xFD)
                            s->op_state = STATE_PRE1;
                        else
                            s->op_state = STATE_OPCODE;
                    } else {
                        s->op_state = STATE_OPCODE;
                    }
                    break;

                case STATE_PRE2:
                    if (s->prev_cycle != CYCLE_MEMRD) {
                        snprintf(s->mnemonic, sizeof(s->mnemonic), "Missing displacement");
                        s->ann_dasm = ANN_WARN;
                        s->op_state = STATE_RESTART;
                        break;
                    }
                    s->op_prefix = (s->op_prefix << 8) | s->pend_data;
                    s->op_state = STATE_PREDIS;
                    break;

                case STATE_PREDIS:
                    if (s->prev_cycle != CYCLE_MEMRD) {
                        snprintf(s->mnemonic, sizeof(s->mnemonic), "Missing opcode");
                        s->ann_dasm = ANN_WARN;
                        s->op_state = STATE_RESTART;
                        break;
                    }
                    s->arg_dis = signed_byte(s->pend_data);
                    s->op_state = STATE_OPCODE;
                    break;

                case STATE_OPCODE: {
                    int table_idx;
                    if (s->op_prefix == 0) table_idx = 0;
                    else if (s->op_prefix == 0xED) table_idx = 1;
                    else if (s->op_prefix == 0xCB) table_idx = 2;
                    else if (s->op_prefix == 0xDD) table_idx = 3;
                    else if (s->op_prefix == 0xFD) table_idx = 4;
                    else if (s->op_prefix == 0xDDCB) table_idx = 5;
                    else if (s->op_prefix == 0xFDCB) table_idx = 6;
                    else table_idx = 0;

                    const struct z80_instr *table = instr_table_by_prefix[table_idx];
                    const struct z80_instr *instr = &table[s->pend_data & 0xFF];

                    /* Set register name */
                    if (table_idx >= 3 && table_idx <= 6)
                        strncpy(s->arg_reg, reg_by_prefix[table_idx], sizeof(s->arg_reg) - 1);
                    else
                        s->arg_reg[0] = '\0';

                    s->op_prefix = 0;

                    if (instr->mnemonic == NULL) {
                        snprintf(s->mnemonic, sizeof(s->mnemonic), "Invalid instruction");
                        s->ann_dasm = ANN_WARN;
                        s->op_state = STATE_RESTART;
                        break;
                    }

                    s->want_dis = instr->want_dis;
                    s->want_imm = instr->want_imm;
                    s->want_read = instr->want_read;
                    s->want_write = abs(instr->want_write);
                    s->want_wr_be = (instr->want_write < 0) ? 1 : 0;
                    s->op_repeat = instr->op_repeat;
                    strncpy(s->mnemonic, instr->mnemonic, sizeof(s->mnemonic) - 1);

                    if (s->want_dis > 0)
                        s->op_state = STATE_POSTDIS;
                    else if (s->want_imm > 0)
                        s->op_state = STATE_IMM1;
                    else {
                        s->ann_dasm = ANN_INSTR;
                        if (s->want_read > 0 && (s->prev_cycle == CYCLE_MEMRD || s->prev_cycle == CYCLE_IORD))
                            s->op_state = STATE_ROP1;
                        else if (s->want_write > 0 && (s->prev_cycle == CYCLE_MEMWR || s->prev_cycle == CYCLE_IOWR))
                            s->op_state = STATE_WOP1;
                        else
                            s->op_state = STATE_RESTART;
                    }
                    break;
                }

                case STATE_POSTDIS:
                    s->arg_dis = signed_byte(s->pend_data);
                    if (s->want_imm > 0)
                        s->op_state = STATE_IMM1;
                    else {
                        s->ann_dasm = ANN_INSTR;
                        if (s->want_read > 0 && (s->prev_cycle == CYCLE_MEMRD || s->prev_cycle == CYCLE_IORD))
                            s->op_state = STATE_ROP1;
                        else if (s->want_write > 0 && (s->prev_cycle == CYCLE_MEMWR || s->prev_cycle == CYCLE_IOWR))
                            s->op_state = STATE_WOP1;
                        else
                            s->op_state = STATE_RESTART;
                    }
                    break;

                case STATE_IMM1:
                    s->arg_imm = s->pend_data;
                    if (s->want_imm > 1)
                        s->op_state = STATE_IMM2;
                    else {
                        s->ann_dasm = ANN_INSTR;
                        if (s->want_read > 0 && (s->prev_cycle == CYCLE_MEMRD || s->prev_cycle == CYCLE_IORD))
                            s->op_state = STATE_ROP1;
                        else if (s->want_write > 0 && (s->prev_cycle == CYCLE_MEMWR || s->prev_cycle == CYCLE_IOWR))
                            s->op_state = STATE_WOP1;
                        else
                            s->op_state = STATE_RESTART;
                    }
                    break;

                case STATE_IMM2:
                    s->arg_imm |= s->pend_data << 8;
                    s->ann_dasm = ANN_INSTR;
                    if (s->want_read > 0 && (s->prev_cycle == CYCLE_MEMRD || s->prev_cycle == CYCLE_IORD))
                        s->op_state = STATE_ROP1;
                    else if (s->want_write > 0 && (s->prev_cycle == CYCLE_MEMWR || s->prev_cycle == CYCLE_IOWR))
                        s->op_state = STATE_WOP1;
                    else
                        s->op_state = STATE_RESTART;
                    break;

                case STATE_ROP1:
                    s->arg_read = s->pend_data;
                    if (s->want_read < 2) {
                        snprintf(s->mnemonic, sizeof(s->mnemonic), "{ro:02X}");
                        s->ann_dasm = ANN_ROP;
                    }
                    if (s->want_write > 0)
                        s->op_state = STATE_WOP1;
                    else if (s->want_read > 1)
                        s->op_state = STATE_ROP2;
                    else if (s->op_repeat && (s->prev_cycle == CYCLE_MEMRD || s->prev_cycle == CYCLE_IORD))
                        s->op_state = STATE_ROP1;
                    else
                        s->op_state = STATE_RESTART;
                    break;

                case STATE_ROP2:
                    s->arg_read |= s->pend_data << 8;
                    snprintf(s->mnemonic, sizeof(s->mnemonic), "{ro:04X}");
                    s->ann_dasm = ANN_ROP;
                    if (s->want_write > 0 && (s->prev_cycle == CYCLE_MEMWR || s->prev_cycle == CYCLE_IOWR))
                        s->op_state = STATE_WOP1;
                    else
                        s->op_state = STATE_RESTART;
                    break;

                case STATE_WOP1:
                    s->arg_write = s->pend_data;
                    if (s->want_read > 1)
                        s->op_state = STATE_ROP2;
                    else if (s->want_write > 1)
                        s->op_state = STATE_WOP2;
                    else {
                        snprintf(s->mnemonic, sizeof(s->mnemonic), "{wo:02X}");
                        s->ann_dasm = ANN_WOP;
                        if (s->want_read > 0 && s->op_repeat &&
                            (s->prev_cycle == CYCLE_MEMRD || s->prev_cycle == CYCLE_IORD))
                            s->op_state = STATE_ROP1;
                        else
                            s->op_state = STATE_RESTART;
                    }
                    break;

                case STATE_WOP2:
                    if (s->want_wr_be)
                        s->arg_write = (s->arg_write << 8) | s->pend_data;
                    else
                        s->arg_write |= s->pend_data << 8;
                    snprintf(s->mnemonic, sizeof(s->mnemonic), "{wo:04X}");
                    s->ann_dasm = ANN_WOP;
                    s->op_state = STATE_RESTART;
                    break;

                case STATE_RESTART:
                    s->op_state = STATE_IDLE;
                    break;
                }

                if (s->ann_dasm != -1)
                    put_disasm(di, s, di_samplenum(di));
                if (s->op_state == STATE_RESTART)
                    s->op_state = STATE_IDLE;

                if (s->ann_data != -1) {
                    char data_str[8];
                    snprintf(data_str, sizeof(data_str), "%02X", s->pend_data);
                    put_text(di, s, s->data_start, s->ann_data, data_str);
                }
                s->data_start = di_samplenum(di);
                s->pend_data = s->bus_data;
                s->ann_data = ann_data_cycle_map[s->prev_cycle];
            } else {
                /* Cycle transition */
                put_text(di, s, di_samplenum(di) - 1, ANN_WARN,
                         "Illegal transition between control states");
                s->pend_addr = -1;
                s->ann_data = -1;
                s->ann_dasm = -1;
            }
        }
        s->prev_cycle = cycle;
    }
}

static void z80_destroy(struct srd_decoder_inst *di)
{
    void *priv = c_decoder_get_private(di);
    if (priv) {
        g_free(priv);
        c_decoder_set_private(di, NULL);
    }
}

